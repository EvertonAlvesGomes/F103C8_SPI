/*
 * This project was developed for test SPI communication between STM32F103C8
 * microcontroller as a master with a SPI slave. The tests was performed with an
 * Arduino Nano board acting as a slave.
 *
 * The STM32F103C8 has two SPI units, and this project implements the SPI_1.
 * The configuration used is:
 * - SPI MODE 0: CPOL = 0 and CPHA = 0
 * - MSBit is transferred first, LSBit last
 * - Data size: 8 bits
 * - Base clock frequency (HCLK): 32 MHz
 * - Baudrate prescaler: 8, producing a SPI clock with 500 kbit/s
 * - NSS pin controlled by hardware and used as output
 */
/*--------------------------------------------------------------------------------*/
/*
 * STM32F103C8 SPI_1 Pinout:
 * NSS = PA4
 * SCK = PA5
 * MISO = PA6
 * MOSI = PA7
 */

